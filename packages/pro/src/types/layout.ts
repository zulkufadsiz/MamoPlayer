/**
 * Visual layout variant for the player UI.
 *
 * - `'compact'`  — Minimal controls, suited for small embedded players.
 * - `'standard'` — Full controls bar with transport buttons and timeline.
 * - `'ott'`      — OTT/streaming-app style with bottom-anchored controls and a larger scrubber.
 */
export type PlayerLayoutVariant = 'compact' | 'standard' | 'ott';
